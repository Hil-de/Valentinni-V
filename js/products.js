function getQueryParam(param) {
    const urlParams = new URLSearchParams(window.location.search);
    return urlParams.get(param);
}

const productId = getQueryParam('id');

if (products[productId]) {
    // Obtener los elementos HTML donde se mostrará la información
    const productName = document.getElementById("product_name");
    const productPrice = document.getElementById("price");
    const productDetails = document.getElementById("details");
    const productDescription = document.getElementById("description");
    const productMaterials = document.getElementById("materials");
    const productImages = document.querySelectorAll("#product_img"); // Obtener todas las imágenes

    // Actualizar el contenido con los datos del producto seleccionado
    productName.textContent = products[productId].name;
    productPrice.textContent = products[productId].price;
    productDetails.innerHTML = products[productId].details; // Usar innerHTML para permitir HTML
    productDescription.innerHTML = products[productId].description;
    productMaterials.innerHTML = products[productId].materials;

    // Actualizar las imágenes
    products[productId].images.forEach((imgSrc, index) => {
        if (productImages[index]) {
            productImages[index].src = imgSrc; // Cambiar el src de cada imagen
            productImages[index].alt = `${products[productId].name} imagen ${index + 1}`; // Alternativa para accesibilidad
        }
    });
} else {
    // Si no existe el producto, mostrar un mensaje de error
    const productName = document.getElementById("product_name");
    productName.textContent = "Producto no encontrado";
}